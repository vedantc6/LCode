# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution:
    def invertTree(self, root):
        """
        :type root: TreeNode
        :rtype: TreeNode
        """
        if not root:
            return None

        queue = [root]
        while queue:
            current = queue.pop()
            temp = current.left
            current.left = current.right
            current.right = temp

            if current.right:
                queue.append(current.right)
            if current.left:
                queue.append(current.left)
        return root
